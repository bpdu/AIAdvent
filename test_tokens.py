import requests
import json
from dotenv import load_dotenv
import os

# Load environment variables
load_dotenv(dotenv_path='.secrets/deepseek-api-key.env')

DEEPSEEK_API_KEY = os.getenv('DEEPSEEK_API_KEY')
DEEPSEEK_API_URL = 'https://api.deepseek.com/v1/chat/completions'
# Using DeepSeek Chat - paid model with better reliability
MODEL_NAME = 'deepseek-chat'  # Main DeepSeek model


def test_deepseek_api(prompt: str, description: str) -> dict:
    """–û—Ç–ø—Ä–∞–≤–∏—Ç—å –∑–∞–ø—Ä–æ—Å –∫ DeepSeek API –∏ –≤–µ—Ä–Ω—É—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É"""

    headers = {
        'Authorization': f'Bearer {DEEPSEEK_API_KEY}',
        'Content-Type': 'application/json'
    }

    payload = {
        "model": MODEL_NAME,
        "messages": [
            {
                "role": "user",
                "content": prompt
            }
        ],
        "temperature": 0.7,
        "max_tokens": 2000
    }

    print(f"\n{'='*60}")
    print(f"üìù {description}")
    print(f"{'='*60}")
